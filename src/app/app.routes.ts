import { Routes } from '@angular/router';

export const routes: Routes = [
  {
    path: 'auth',
    loadChildren: () =>
      import('./auth/auth.routes').then((routes) => routes.authRoutes),
  },
  {
    path: 'storage',
    loadChildren: () =>
      import('./storage/storage.routes').then((routes) => routes.storageRoutes),
  },
  {
    path: '**',
    redirectTo: 'auth',
  },
];
